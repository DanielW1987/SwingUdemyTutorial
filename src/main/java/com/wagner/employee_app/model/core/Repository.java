package com.wagner.employee_app.model.core;

import java.sql.SQLException;
import java.util.List;
import java.util.Optional;

public interface Repository<T> {
  ////////////////////// region Variables  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // endregion
  ////////////////////// region Methods ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  List<T> getAll() throws SQLException;

  Optional<T> getById(Long id) throws SQLException;

  Optional<T> getByName(String name) throws SQLException;

  List<T> getAllWithNameLike(String name) throws SQLException;

  boolean remove(Long id) throws SQLException;

  boolean update(T element) throws SQLException;

  Long create(T element) throws SQLException;
  // endregion
  ////////////////////// region Inner Classes //////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  ////////////////////// End of Class //////////////////////////////////////////////////////////////////////////////////////////////////////////////// endregion
}